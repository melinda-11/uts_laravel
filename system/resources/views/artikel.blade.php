					<p>Tak hanya pesona sungai Kapuas sebagai sungai terpanjang, ternyata Kalimantan Barat ini juga memiliki beberapa wisata air terjun. Salah satu wisata air terjun yang menjadi incaran pengunjung adalah Air Terjun Riam Merasap. Air terjun yang satu ini terletak di desa Segonde Batang Air, Kecamatan Sanggau Ledo, Kabupaten Bengkayang Provinsi Kalimantan Barat.</p>
					<p>Air Terjun Riam Merasap memiliki ketinggian mencapai lebih dari 20 meter dan lebar sekitar 8meter. Berdasarkan sejarahnya, konon penamaan Riam Merasap diambil dari aliran yang jatuh dari air terjun ini memiliki aliran air yang  deras. Bahkan alirannya sampai menimbulkan kabut asap yang dingin dari jatuhnya percikan air. Saking tinggi dan lebarnya air terjun ini, masyarakat setempat menyebutnya sebagai miniatur dari Air terjun Niagara yang berada di negara adidaya Amerika Serikat.Â </p>
					<p>Selain berfungsi sebagai objek wisata, air terjun Riam Merasap juga dijadikan sebagai salah satu sumber listrik yang dibangun oleh PLTA. Uniknya lagi, di tempat ini juga dijadikan salah satu lokasi ibadah bagi umat katolik. Sebab lokasinya yang berada tepat diatas Gua Maria. Lokasi Air Terjun Merasap berada pada kawasan yang asri , yakni terletak ditengah-tengah hutan tropis Kalimantan. Tak heran jika pemandangan yang disajikan masih begitu natural dan begitu terjaga kondisinya.</p>
					<p>Berlokasi sekitar 300km dari Kota Pontianak,daya pikatnya tak menyurutkan pelancong yang datang dari jauh untuk sekedar singgah di air terjun tersebut. Apalagi lokasinya yang berdekatan dengan perbatasan Kota Malaysia sekitar 10km ini menjadi obyek wisata mancanegara bagi negara tetangga. Jika kamu dan keluarga singgah ke Kalimantan barat, tak ada salahnya kamu mampir ke mini Niagaranya Indonesia ini.</p>
					
